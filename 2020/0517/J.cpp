signed main()
{
    ifastios();
    uint h,b,t;
    while (cin>>h>>b>>t)
    {
        static iint a[4];
        for (int i=1;i<=3;i++) read(a[i]);
        uint ans=0;
        static uint f[1000050];
        memset(f,0,sizeof(f));
        for (int i=0;i<=h;i++)
        {
            double x=b-1.0*(b-t)*i/h;
            uint ix=(iint)x;
            for (int j=1;j<=3;j++)
                if (i>=a[j]) f[i]=max(f[i],f[i-a[j]]+(ix/a[j])*(ix/a[j])*(a[j]*a[j]*a[j]));
            ans=max(ans,f[i]);
        }
        print(ans,'\n');
    }
}